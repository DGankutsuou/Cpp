#include<iostream>
#include<cmath>
using namespace std;
int main(){

    cout<<max(12,23)<<endl; // max function print the maximum number.
    cout<<min(12,23)<<endl; // min function print the minimum number.
    cout<<pow(2,3)<<endl; // 2^3 = 8
    cout<<sqrt(16)<<endl; // V16 = 4
    cout<<ceil(3.5)<<endl; // 3.5 will be 4
    cout<<floor(3.5)<<endl; // 3.5 will be 3
    cout<<fabs(-34)<<endl; // |-34| = 34 
    cout<<fmod(16.5,3.7)<<endl; // 16.5 % 3.7 = ?
    cout<<exp(3)<<endl; // exponential = exp = e = 2.718 // e^3 = ?
    cout<<cos(180)<<endl; 
    cout<<sin(90)<<endl;
    cout<<tan(45)<<endl;
    
    return 0;
}